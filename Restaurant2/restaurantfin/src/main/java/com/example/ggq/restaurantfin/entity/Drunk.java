package com.example.ggq.restaurantfin.entity;

public class Drunk {
    private String drunkNumber;
    private String drunkName;
    private String drunkPhoto;
    private Float drunkPirce;

    public String getDrunkNumber() {
        return drunkNumber;
    }

    public void setDrunkNumber(String drunkNumber) {
        this.drunkNumber = drunkNumber;
    }

    public String getDrunkName() {
        return drunkName;
    }

    public void setDrunkName(String drunkName) {
        this.drunkName = drunkName;
    }

    public String getDrunkPhoto() {
        return drunkPhoto;
    }

    public void setDrunkPhoto(String drunkPhoto) {
        this.drunkPhoto = drunkPhoto;
    }

    public Float getDrunkPirce() {
        return drunkPirce;
    }

    public void setDrunkPirce(Float drunkPirce) {
        this.drunkPirce = drunkPirce;
    }

}
